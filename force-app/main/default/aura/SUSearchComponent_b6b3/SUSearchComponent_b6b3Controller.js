    removeStickyFilter: function(component, event, helper) {

        let contentname = event.target.getAttribute("data-Contentname");
        let label = event.target.getAttribute("data-label");
        let level = event.target.getAttribute("data-level");
        let type = event.target.getAttribute("data-type");

        let immediateParent = event.target.getAttribute("data-immediateParent");

        let index = -1;
        let removed = false;
        let selectedRows = Array.from(component.get('v.selectedRows'));

        let mergedArray = component.get('v.mergedArray');

        component.set("v.pageNum", "1");

        selectedRows.some(function(f, i) { if (f.Contentname == contentname && f.label == label && f.parent == type) { index = i; return true; } });

        if (index > -1) {
            selectedRows.splice(index, 1);
        }

        let selectedTypeFilter = JSON.parse(component.get('v.selectedTypeFilter'));
        
        let ind  = -1;
        selectedTypeFilter.some(function(f, i) { if (f.type == type) { ind = i; return true;} });
        
        if(selectedTypeFilter[ind].filter.length){
            
		    if(contentname.indexOf('merged') > -1 || (immediateParent && immediateParent.indexOf('merged') > -1)){
                let p = mergedArray.filter(function(m) { return (contentname == 'merged_'+m.filterNewName || immediateParent == 'merged_'+m.filterNewName)})
                if( p.length ){
                    p[0].filterList.forEach(function (f) {
                        let j = selectedTypeFilter[ind].filter.indexOf(f);
                        if(j > -1) selectedTypeFilter[ind].filter.splice(j,1);
                    })
                }
            }
            else {
                
                let i = selectedTypeFilter[ind].filter.indexOf(contentname); 
            
            	if(i > -1) selectedTypeFilter[ind].filter.splice(i,1); removed = true;
            }
        }

        if (selectedTypeFilter[ind].children && !removed) {

            let i = -1;
            selectedTypeFilter[ind].children.some(function(f, index) { if (f.childName == contentname) { i = index; return true; } });

            if (i > -1) {
                selectedTypeFilter[ind].children.splice(i, 1);
                removed = false;
            }
        }

        if (!selectedTypeFilter[ind].filter.length && (!selectedTypeFilter[ind].children || !selectedTypeFilter[ind].children.length)) {
            selectedTypeFilter.splice(ind, 1);
        }

        component.set('v.selectedTypeFilter', JSON.stringify(selectedTypeFilter));

        component.set('v.selectedRows', selectedRows);

        if(removed && (!immediateParent || !immediateParent.indexOf('merged') > -1)) {
            helper.getValue(component,'filterCheck','true', helper); }
        else {
            var sr = {};
            sr["Contentname"] = contentname;
            sr["immediateParent"] = immediateParent;
            sr["parent"] = type;
            sr["level"] = level;
            sr["checkedProp"] = false;
            sr["checked"] = false;
            helper.typeSelectFunc(component, event, helper, 1, sr);
        }

    },

