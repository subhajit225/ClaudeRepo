  @api
  autoGenTitle(data, summaryData, retryPopup, time = 1) {
    if (this.displayTitleLoader || this.displaySummaryLoader) {
      let config = JSON.parse(JSON.stringify(this.kcssupport));
      if (typeof summaryData === 'object' && summaryData !== null) {
        for (let index = 0; index < config.length; index++) {
          if (config[index].RecordType == this.recordtype) {
            const mapConfig = config[index].autoGenConfig.config;
            const summaryDataObjKeys = Object.keys(summaryData);
            const mapConfigFields = mapConfig.map(config => config.field);
            const missing = mapConfigFields.filter(key => key !== 'Title' && !summaryDataObjKeys.includes(key));

            if (missing && missing.length) {
              this.template.querySelector(this.knowblerModalComponent).showOpenApiError(2);
              break;
            }
          }
        }
      }
      if (data === 'API Error' || summaryData === 'API Error') {
        if(retryPopup) {
          this.template.querySelector(this.knowblerModalComponent).showOpenApiRetryError();
        } else {
          if (summaryData === 'contextLimitReached') {
            this.template.querySelector(this.knowblerModalComponent).showOpenApiError(1);
          } else {
            this.template.querySelector(this.knowblerModalComponent).showOpenApiError();
          }
        }
        this.displayTitleLoader = false;
        this.displaySummaryLoader = false;
        let tempGenerateSummary={...this.generateSummaryFields};
        Object.keys(tempGenerateSummary).forEach((field)=>{
          tempGenerateSummary[field]=false;
        })
        // Object.keys(this.generateSummaryFields).forEach((field)=>{
        //   this.generateSummaryFields[field]=false;
        // })
        this.generateSummaryFields={...tempGenerateSummary};
        return;
      }

      config.forEach((element) => {
        if (element.RecordType == this.recordtype) {
          const mappingConfig = element.autoGenConfig.config;
          if (this.mapping.length && mappingConfig.length) {
            for (let i = 0; i < this.mapping.length; i++) {
              if (this.mapping[i].autoGen) {
                const existingPacket = mappingConfig.find(
                  (item) => item.field === this.mapping[i].name
                );
                if (existingPacket) {
                  if (
                    existingPacket.generateType === 'titleGeneration' &&
                    this.displayTitleLoader
                  ) {
                    if (this.mapping[i].isRichTextArea) {
                      const contentWithBr = data.replace(/\n/g, '<br>');
                      this.mapping[i].value = contentWithBr;
                      this.mapping[i].label = existingPacket.label;
                      this.mapping[i].sectionLabel =
                        existingPacket.sectionLabel;
                      this.fieldset[this.mapping[i].name] = contentWithBr;
                    } else {
                      this.mapping[i].value = data;
                      this.mapping[i].label = existingPacket.label;
                      this.mapping[i].sectionLabel =
                        existingPacket.sectionLabel;
                      this.fieldset[this.mapping[i].name] = data;
                      if (this.mapping[i].name === "Title") {
                        this.handleChange("Title");
                      }
                    }
                    this.displayTitleLoader = false;
                  } else if (
                    existingPacket.generateType != 'titleGeneration' &&
                    this.generateSummaryFields.hasOwnProperty(existingPacket.generateType)
                    // this.displaySummaryLoader
                  ) {
                    if (this.mapping[i].isRichTextArea) {
                      const contentWithBr = summaryData[existingPacket.generateType].replace(/\n/g, '<br>');
                      this.mapping[i].value = contentWithBr;
                      this.mapping[i].label = existingPacket.label;
                      this.mapping[i].sectionLabel =
                        existingPacket.sectionLabel;
                      this.fieldset[this.mapping[i].name] = contentWithBr;
                    } else {
                      this.mapping[i].value = summaryData[existingPacket.generateType];
                      this.mapping[i].label = existingPacket.label;
                      this.mapping[i].sectionLabel =
                        existingPacket.sectionLabel;
                      this.fieldset[this.mapping[i].name] = summaryData[existingPacket.generateType];
                      if (this.mapping[i].name === "Title") {
                        this.handleChange("Title");
                      }
                    }
                    let tempFieldSummary={...this.generateSummaryFields};
                    tempFieldSummary[existingPacket.generateType] = false;
                    this.generateSummaryFields = { ...tempFieldSummary};

                    // this.generateSummaryFields[existingPacket.generateType] = false;
                    this.displaySummaryLoader=false;
                  }
                  this.handleChange('setValue');
                  if (this.mapping[i].value) {
                    this.titleGenerated = true;
                    setTimeout(() => {
                      this.titleGenerated = false;
                    }, 3000);
                  }
                  }
                }
              }
            } else {
              setTimeout(() => {
                if (time === 1) {
                  this.autoGenTitle(data, summaryData, retryPopup, time + 1);
                }
              }, 1000);
            }
          }
      });
    }
  }

