  addBranchIntoTree(pathArray, tree) {
    let found = 0;
    for (const val of tree) {
      if (pathArray[0] === val.name) {
        found = 1;
        pathArray = pathArray.splice(1);
        const res = this.addBranchIntoTree(pathArray, val.childCategories);
        if (res && res.length) {
          val.childCategories = res;

          return tree;
        }
        val.childCategories.push(res);

        return tree;
      }
    }
    if (!found) {
      let obj = { name: pathArray[pathArray.length - 1], childCategories: [] };
      pathArray[pathArray.length - 1] = obj;
      let z = pathArray.length - 1;
      while (z > 0) {
        obj = { name: pathArray[z - 1], childCategories: [pathArray[z]] };
        pathArray[z - 1] = obj;
        pathArray.pop();
        z -= 1;
      }

      return pathArray[0];
    }

    return tree;
  }

  findPath(node, nodeName, currentPath = []) {
    // If the current node matches the desired node name, return the current path
    if (node.name === nodeName) {
        return [...currentPath, node.name];
    }

    // If the current node has child categories, recursively search in them
    if (node.childCategories && node.childCategories.length > 0) {
        for (let child of node.childCategories) {
            let path = this.findPath(child, nodeName, [...currentPath, node.name]);
            if (path) {
                return path;
            }
        }
    }

    // If the node is not found, return null
    return null;
}

